#include "common.h"
#include "Capture.h"

/////////////////////////////////////////////////////////////////////////////
Capture::Capture(const POINT &pt1, const POINT &pt2)
{
	pts[0] = pt1;
	pts[1] = pt2;
}

/////////////////////////////////////////////////////////////////////////////
Capture::Capture(const Capture &rhs)
{
	*this = rhs;
}

/////////////////////////////////////////////////////////////////////////////
Capture::~Capture()
{
}

/////////////////////////////////////////////////////////////////////////////
Capture& Capture::operator =(const Capture &rhs)
{
	if (this != &rhs) {
		memcpy(pts, rhs.pts, sizeof(POINT) * 2);
	}

	return *this;
}
