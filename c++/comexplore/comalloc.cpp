#include "stdafx.h"
#include "comalloc.h"

/////////////////////////////////////////////////////////////////////////////
void* comalloc(size_t cb)
{
    return CoTaskMemAlloc(cb);
}

/////////////////////////////////////////////////////////////////////////////
void cofree(void* pv)
{
    CoTaskMemFree(pv);
}
