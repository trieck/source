#include "common.h"
#include "mobject.h"
#include "integer.h"

/////////////////////////////////////////////////////////////////////////////
Integer::Integer(int64_t i) : val(i)
{
}

/////////////////////////////////////////////////////////////////////////////
Integer::Integer(const Integer &rhs)
{
	*this = rhs;
}

/////////////////////////////////////////////////////////////////////////////
Integer::~Integer()
{
}

/////////////////////////////////////////////////////////////////////////////
Integer &Integer::operator =(const Integer &rhs)
{
	if (this != &rhs) {
		val = rhs.val;
	}
	return *this;
}

/////////////////////////////////////////////////////////////////////////////
LPMOBJECT Integer::copy() const
{
	return new Integer(*this);
}

/////////////////////////////////////////////////////////////////////////////
string Integer::toString() const
{
	char buf[BUF_SIZE];
	sprintf(buf, "<integer>%I64d</integer>", val);
	return buf;
}
