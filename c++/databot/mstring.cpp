#include "common.h"
#include "mobject.h"
#include "mstring.h"

/////////////////////////////////////////////////////////////////////////////
String::String(const string &s) : val(s)
{
}

/////////////////////////////////////////////////////////////////////////////
String::String(const String &rhs)
{
	*this = rhs;
}

/////////////////////////////////////////////////////////////////////////////
String::~String()
{
}

/////////////////////////////////////////////////////////////////////////////
String &String::operator =(const String &rhs)
{
	if (this != &rhs) {
		val = rhs.val;
	}

	return *this;
}

/////////////////////////////////////////////////////////////////////////////
LPMOBJECT String::copy() const
{
	return new String(*this);
}

/////////////////////////////////////////////////////////////////////////////
string String::toString() const
{
	string output;
	output = "<string>";
	output += val;
	output += "</string>";
	return output;
}
