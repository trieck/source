#include "common.h"
#include "mobject.h"
#include "float.h"

/////////////////////////////////////////////////////////////////////////////
Float::Float(double d) : val(d)
{
}

/////////////////////////////////////////////////////////////////////////////
Float::Float(const Float &rhs)
{
	*this = rhs;
}

/////////////////////////////////////////////////////////////////////////////
Float::~Float()
{
}

/////////////////////////////////////////////////////////////////////////////
Float &Float::operator =(const Float &rhs)
{
	if (this != &rhs) {
		val = rhs.val;
	}

	return *this;
}

/////////////////////////////////////////////////////////////////////////////
LPMOBJECT Float::copy() const
{
	return new Float(*this);
}

/////////////////////////////////////////////////////////////////////////////
string Float::toString() const
{
	char buf[BUF_SIZE];
	sprintf(buf, "<real>%f</real>", val);
	return buf;
}
