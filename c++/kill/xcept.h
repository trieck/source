/////////////////////////////////////////////////////////////////////////////
// 
//	XCEPT.H : simple text exception
//

#ifndef __XCEPT_H__
#define __XCEPT_H__

/////////////////////////////////////////////////////////////////////////////
class Exception
{
// Construction / Destruction
public:
	Exception (LPTSTR fmt, ...);
	~Exception();

	operator LPCTSTR() const;
private:
	tstring _text;
};
/////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////
inline Exception::operator LPCTSTR() const {
	return _text.c_str();
}

#endif // __XCEPT_H__

