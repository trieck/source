#include "global.h"
#include "noise.h"
#include "lex.h"

/////////////////////////////////////////////////////////////////////////////
NoiseList::NoiseList()
{
	load();
}

/////////////////////////////////////////////////////////////////////////////
NoiseList::~NoiseList()
{

}

/////////////////////////////////////////////////////////////////////////////
bool NoiseList::isnoise(const string &term)	const
{
	set<string>::const_iterator it = noise.find(term);
	return it != noise.end();
}

/////////////////////////////////////////////////////////////////////////////
void NoiseList::load()
{
	Lex lexer;
	if (!lexer.open("noise.txt")) {
		error("unable to read noise.txt.");
	}

	const char *tok;
	while ((tok = lexer.gettok()) != NULL) {
		noise.insert(tok);
	}
}

