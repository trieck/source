/////////////////////////////////////////////////////////////////////////////
Timer::Timer()
 : start(GetTickCount())
{
}
/////////////////////////////////////////////////////////////////////////////
Timer::~Timer()
{
}
/////////////////////////////////////////////////////////////////////////////
float Timer::getSeconds() const
{
	DWORD diff = GetTickCount() - start;
	return diff / float(1000);
}