/////////////////////////////////////////////////////////////////////////////
CSize Board::GetDimensions()
{
	CSize sz;

	sz.cx = SQUARE_SIZE * CX_SQUARES;
	sz.cy = SQUARE_SIZE * CY_SQUARES;

	return sz;
}

/////////////////////////////////////////////////////////////////////////////
CRect Board::GetBoundingRect()
{
	CRect rc;

	CSize sz = Board::GetDimensions();

	rc.right = sz.cx + (CX_BORDER * 2);
	rc.bottom = sz.cy + (CY_BORDER * 2);

	return rc;
}