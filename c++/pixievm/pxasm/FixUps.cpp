FixUps::FixUps()
{
}

/////////////////////////////////////////////////////////////////////////////
FixUps::~FixUps()
{
}

/////////////////////////////////////////////////////////////////////////////
void FixUps::add(const char *name, word location, FixUpType type)
{
	FixUp fixup;
	fixup.name = name;
	fixup.location = location;
	fixup.type = type;

	m_fixups.push_back(fixup);
}
